    private void eaPoorButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eaPoorButtonActionPerformed
        EaPoor eap = new EaPoor();
        String eapoorTmaxStr = eaPoorTmaxTextField.getText();

        String eapoorTminStr = eaPoortTminTextField.getText();

        String eapoorRhStr = eaPoorRhTextField.getText();

        if (eapoorTmaxStr.length() > 0 && eapoorTminStr.length() > 0 && eapoorRhStr.length() > 0) {
            double eapoorTmaxD = Double.parseDouble(eapoorTmaxStr);
            double eapoorTminD = Double.parseDouble(eapoorTminStr);
            double eapoorRhD = Double.parseDouble(eapoorRhStr);
            eaPoorResultLabel.setText("" + eap.calcEa(eapoorTminD, eapoorTmaxD, eapoorRhD));
            etoTmaxTextField.setText(eapoorTmaxStr);
            etoTminTextField.setText(eapoorTminStr);
            etoEaTextField.setText(eaPoorResultLabel.getText());
            
        }
    }//GEN-LAST:event_eaPoorButtonActionPerformed

