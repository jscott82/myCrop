    private void eaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eaButtonActionPerformed
        Ea ea = new Ea();
        String eaTmaxStr = eaTmaxTextField.getText();
        String eaTminStr = eaTminTextField.getText();
        String eaRHMaxStr = eaRHMaxTextField.getText();
        String eaRHMinStr = eaRHMinTextField.getText();
        if (eaTmaxStr.length() > 0 && eaTminStr.length() > 0 && eaRHMaxStr.length() > 0 && eaRHMinStr.length() > 0) {
            double eaTmaxD = Double.parseDouble(eaTmaxStr);
            double eaTminD = Double.parseDouble(eaTminStr);
            double eaRHMaxD = Double.parseDouble(eaRHMaxStr);
            double eaRHMinD = Double.parseDouble(eaRHMinStr);
            eaResultsLabel.setText("" + ea.calcEa(eaTminD, eaTmaxD, eaRHMinD, eaRHMaxD));
            etoTmaxTextField.setText(eaTmaxStr);
            etoTminTextField.setText(eaTminStr);
            etoEaTextField.setText(eaResultsLabel.getText());

        }
    }//GEN-LAST:event_eaButtonActionPerformed

    private void calcEToButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcEToButtonActionPerformed
        String etoTmaxStr = etoTmaxTextField.getText();
        String etoTminStr = etoTminTextField.getText();
        String etoEaStr = etoEaTextField.getText();
        String etoAirSpeedStr = etoAirSpeedTextField.getText();
        String etoMonthNumberStr = etoMonthNumberTextField.getText();
        String etoLatDegreesStr = etoLatDegreesTextField.getText();
        String etoLatitudeMinutes = etoLatitudeMinutesTextField.getText();
        String etoAvMonTempStr = etoAvMonTempTextField.getText();
        String etoPrevMonthTempStr = etoPrevMonthTempTextField.getText();
        String etoAltitudeStr = etoAltitudeTextField.getText();
        String etoSunHoursStr = etoSunHoursTextField.getText();
        if (etoTmaxStr.length() > 0 && etoTminStr.length() > 0 && etoEaStr.length() > 0 && etoAirSpeedStr.length() > 0 && etoMonthNumberStr.length() > 0 && etoLatDegreesStr.length() > 0 && etoLatitudeMinutes.length() > 0 && etoAvMonTempStr.length() > 0 && etoPrevMonthTempStr.length() > 0 && etoAltitudeStr.length() > 0 && etoSunHoursStr.length() > 0) {
            double etoTmaxD = Double.parseDouble(etoTmaxStr);
            double etoTminD = Double.parseDouble(etoTminStr);
            double etoEaD = Double.parseDouble(etoEaStr);
            double etoAirSpeedD = Double.parseDouble(etoAirSpeedStr);
            int etoMonthNumberI = Integer.parseInt(etoMonthNumberStr);
            int etoLatDegreesI = Integer.parseInt(etoLatDegreesStr);
            int etoLatitudeI = Integer.parseInt(etoLatitudeMinutes);
            double etoAvMonTempD = Double.parseDouble(etoAvMonTempStr);
            double etoPrevMonthTempD = Double.parseDouble(etoPrevMonthTempStr);
            double etoAltitudeD = Double.parseDouble(etoAltitudeStr);
            double etoSunHoursD = Double.parseDouble(etoSunHoursStr);
            PenmanMonteith pm = new PenmanMonteith(etoTmaxD, etoTminD, etoEaD, etoAirSpeedD, 15, etoMonthNumberI, etoLatDegreesI, etoLatitudeI, etoAvMonTempD, etoPrevMonthTempD, etoAltitudeD, etoSunHoursD);
            etoResultsLabel.setText("" + pm.calculate());
        }
    }//GEN-LAST:event_calcEToButtonActionPerformed

    private void calcETButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcETButtonActionPerformed
        String etoKcStr = etoKcTextField.getText();
        String etoResultsStr=etoResultsLabel.getText();
        if(etoKcStr.length()>0&&etoResultsStr.length()>0)
        {
            double kc=Double.parseDouble(etoKcStr);
            double eto=Double.parseDouble(etoResultsStr);
            etResultsLabel.setText(""+kc*eto);
        }
    }//GEN-LAST:event_calcETButtonActionPerformed

